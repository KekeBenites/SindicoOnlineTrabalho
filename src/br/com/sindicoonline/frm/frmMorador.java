    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (txtIdMorador.equals(null)) {
            JOptionPane.showMessageDialog(null, "Selecionar algum morador.");
        } else {

            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", title, JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {

                try {
                    Morador morador = new Morador();
                    MoradorDAO daoMorador = new MoradorDAO();
                    morador.setIdMorador(Integer.valueOf(txtIdMorador.getText()));
                    JOptionPane.showMessageDialog(this, "Morador excluido com ");
                    daoMorador.deleteMorador(morador);

                    txtIdMorador.setText(null);
                    txtNomeMorador.setText(null);
                    txtCPF.setText(null);
                    txtUsuario.setText(null);
                    txtSenha.setText(null);

                    lerTabela();

                } catch (Exception e) {
                }

            }

        }
        btnCancelar.setEnabled(false);

    }//GEN-LAST:event_btnExcluirActionPerformed

