    public void lerTabela() {

        DefaultTableModel modelo = (DefaultTableModel) jtMorador.getModel();
        modelo.setNumRows(0);
        Morador morador = new Morador();
        MoradorDAO daoMorador = new MoradorDAO();

        for (Morador m : daoMorador.readMoradorListaComNome(morador)) {
            modelo.addRow(new Object[]{
                m.getIdMorador(),
                m.getNomeMorador(),
                m.getCpfMorador(),
                m.getApto().getNumeroApto(),
                m.getBloco().getNumeroBloco(),
                m.getPredio().getNomePredio(),
                m.getLoginMorador(),
                m.getSenhaMorador()});

        }

    }

