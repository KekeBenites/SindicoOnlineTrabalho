    public void limpaEPopulaBloco() {
        try {

            Utilidades util = new Utilidades();

            comboBloco.removeAllItems();
            int codPredio = util.achaCodigoPredio((String) comboPredio.getSelectedItem());

            populaComboBloco(codPredio);
        } catch (Exception e) {

        }
    }

    public void limpaEPopulaApto() {
        try {

            Utilidades util = new Utilidades();

            int codPredio = util.achaCodigoPredio((String) comboPredio.getSelectedItem());
            int codBloco = util.achaCodigoBloco((String) comboBloco.getSelectedItem(), codPredio);

            comboApto.removeAllItems();
            populaComboApto(codBloco);
        } catch (Exception e) {

        }

    }

    public void populaComboSemFiltro() {
        try {
            Utilidades util = new Utilidades();

            int codPredio = util.achaCodigoPredio((String) comboPredio.getSelectedItem());
            int codBloco = util.achaCodigoBloco((String) comboBloco.getSelectedItem(), codPredio);
            comboApto.removeAllItems();
            populaComboAptoTodos(codBloco);

        } catch (Exception e) {

        }

    }

    public void populaComboPredio() {

        PreparedStatement stmt = null;
        Connection con = Conexao.getConnection();
        ResultSet rs = null;
        try {
            stmt = con.prepareStatement("SELECT * FROM predio");
            rs = stmt.executeQuery();
            while (rs.next()) {
//                Esse funciona q é um abeleza.
                comboPredio.addItem(rs.getObject(2));

            }
            con.close();
        } catch (Exception e) {

        }

    }

