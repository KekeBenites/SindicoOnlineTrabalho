    public void lerTabela() {

        DefaultTableModel modelo = (DefaultTableModel) jtApto.getModel();
        modelo.setNumRows(0);
        Apto apto = new Apto();
        AptoDAO daoApto = new AptoDAO();

        for (Apto a : daoApto.readAptoListaComNome(apto)) {
            modelo.addRow(new Object[]{
                a.getIdApto(),
                a.getNumeroApto(),
                a.getBloco().getNumeroBloco(),
                a.getPredio().getNomePredio()});

        }

    }

