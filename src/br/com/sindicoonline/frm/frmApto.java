    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Apto apto = new Apto();
        AptoDAO daoApto = new AptoDAO();
        Bloco b = new Bloco();
        Utilidades util = new Utilidades();

        int codPredio = util.achaCodigoPredio((String) comboPredio.getSelectedItem());
        int codBloco = util.achaCodigoBloco((String) comboBloco.getSelectedItem(), codPredio);

        b.setIdBloco(codBloco);

        apto.setNumeroApto(Integer.valueOf(txtNumeroApto.getText()));
        apto.setBloco(b);

        try {

            if (opSAlvarEditar.equals("salvar")) {
                daoApto.adicionarApto(apto);
                JOptionPane.showMessageDialog(null, "Apartamento salvo com sucesso!");
            } else {
                apto.setIdApto(Integer.valueOf(txtIdApto.getText()));
                daoApto.updateApto(apto);
                JOptionPane.showMessageDialog(null, "Apartamento alterado com sucesso!");
            }

            txtNumeroApto.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnNovo.setEnabled(true);
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            btnNovo.requestFocus();
            comboBloco.setEnabled(false);
            comboPredio.setEnabled(false);
            btnCancelar.setEnabled(false);
            txtNumeroApto.setEnabled(false);
            btnNovo.requestFocus();
            jtApto.setEnabled(true);
            lerTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao Salvar" + e);
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

