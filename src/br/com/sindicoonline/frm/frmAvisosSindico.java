    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        Aviso aviso = new Aviso();
        AvisoDAO daoAviso = new AvisoDAO();
        
        
        try{
            aviso.setIdAviso(Integer.valueOf(txtId.getText()));
            daoAviso.deleteAviso(aviso);
            
            JOptionPane.showMessageDialog(null, "Aviso excluido com sucesso.");
            txtAvisos.setText("");
            txtId.setText("");
            btnEditar.setEnabled(false);
            btnEnviar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnNovo.requestFocus();
            
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Erro ao excluir.");
        }
        
        lerTabela();

    }//GEN-LAST:event_btnExcluirActionPerformed
    public void populaCombo() {
        PreparedStatement stmt = null;
        Connection con = Conexao.getConnection();
        ResultSet rs = null;
        try {
            stmt = con.prepareStatement("SELECT * FROM predio");
            rs = stmt.executeQuery();
            while (rs.next()) {
//                Esse funciona q Ã© um abeleza.
                comboPredio.addItem(rs.getObject(2));
            }
        } catch (Exception e) {
        }
    }
