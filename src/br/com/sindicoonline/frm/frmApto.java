    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        jtApto.setEnabled(false);
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente EXCLUIR?", title, JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            try {
                Apto apto = new Apto();
                AptoDAO daoApto = new AptoDAO();
                apto.setIdApto(Integer.valueOf(txtIdApto.getText()));
                daoApto.deleteApto(apto);
                JOptionPane.showMessageDialog(null, "Apto excluido com sucesso.");
            } catch (Exception e) {
            }

            txtNumeroApto.setEnabled(false);
            btnSalvar.setEnabled(false);
            btnNovo.setEnabled(true);
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnNovo.requestFocus();
            comboBloco.setEnabled(false);
            comboPredio.setEnabled(false);
            btnCancelar.setEnabled(false);
            txtNumeroApto.setText(null);
            txtIdApto.setText(null);
            btnNovo.requestFocus();
            txtNumeroApto.setEnabled(false);
            lerTabela();

        }
        jtApto.setEnabled(true);
    }//GEN-LAST:event_btnExcluirActionPerformed

